 class Generic<X, Y>
{
    X variable1;
    Y variable2;
    public Generic(X variable1, Y variable2)
    {
        this.variable1 = variable1;
        this.variable2 = variable2;
    }
    public X getVariable1()
    {
        return variable1;
    }
    public Y getVariable2()
    {
        return variable2;
    }
}

